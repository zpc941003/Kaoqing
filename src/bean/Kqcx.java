package bean;

public class Kqcx {

	private int kq;
	private int jq;
	private int cc;
	private int jb;
	private int tx;
	public Kqcx(){
		
	}
	public Kqcx(int kq, int jq, int cc, int jb, int tx) {
		//super();
		this.kq = kq;
		this.jq = jq;
		this.cc = cc;
		this.jb = jb;
		this.tx = tx;
	}
	public int getKq() {
		return kq;
	}
	public void setKq(int kq) {
		this.kq = kq;
	}
	public int getJq() {
		return jq;
	}
	public void setJq(int jq) {
		this.jq = jq;
	}
	public int getCc() {
		return cc;
	}
	public void setCc(int cc) {
		this.cc = cc;
	}
	public int getJb() {
		return jb;
	}
	public void setJb(int jb) {
		this.jb = jb;
	}
	public int getTx() {
		return tx;
	}
	public void setTx(int tx) {
		this.tx = tx;
	}
	
}
